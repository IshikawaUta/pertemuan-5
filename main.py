def sapa(nama, hobi, tangal_lahir):
    print("Halo,", nama, "Hobi", hobi, "Tangal lahir", tangal_lahir, "!")
# format string tanpa jarak spasi
    print(f"Halo, {nama}!")
# menampilkan function

# mengisi nilai fungsi dengan tipe data string yaitu andi
sapa("Eka", "play gamer", 20)
# output: Halo, Eka!